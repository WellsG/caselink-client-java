package com.example.caselink;

public class Linkage {

	private String autocase_pattern;
	private String title;
	private String workitem;

	public String getAutocase_pattern() {
		return autocase_pattern;
	}
	public void setAutocase_pattern(String autocase_pattern) {
		this.autocase_pattern = autocase_pattern;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWorkitem() {
		return workitem;
	}
	public void setWorkitem(String workitem) {
		this.workitem = workitem;
	}

}
